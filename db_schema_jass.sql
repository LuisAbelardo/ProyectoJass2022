            END IF;
  
        END LOOP;
        COMMIT;

        CALL sp_calcula_montos_rbos(_fecFacturacion);
        SET code_rpta =  200;
    
    END IF;
     
    -- CIERRO CURSOR
    CLOSE contratos;
 END $$
