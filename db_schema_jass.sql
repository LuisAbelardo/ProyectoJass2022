                            FROM EGRESO 
                            WHERE EGR_ESTADO = 1 
                            AND DATE(EGR_CREATED) BETWEEN _fechaInicio AND _fechaFin);

    SET saldoNetoRangoFecha =  (ingresosRangoFecha - egresosRangoFecha);  

